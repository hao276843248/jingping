I_A_Z_char_dict = {v: i + 1 for i, v in enumerate(A_Z_char)}
I_0_9_char_dict = {i + 1: v for i, v in enumerate(A_Z_char)}


def number_to_char(num, figure=0, char=""):
    """
    数字转excel字母列
    :param num:
    :param figure:
    :param char:
    :return:
    """
    figure = num // 26
    gewi = num % 26
    if gewi == 0:
        char = "Z" + char
        figure -= 1
    else:
        char = I_0_9_char_dict[gewi] + char
    if figure > 0:
        return number_to_char(figure, 0, char)
    else:
        return char


def ret_row_column(value):
    """
    excel行列坐标 转换数值坐标
    :param value: A1
    :return: 1,1
    """
    regex = re.compile("[A-Z]+")
    regex2 = re.compile("[0-9]+")
    column = regex.findall(value)[0]
    column_num = 0
    for i, v in enumerate(column[::-1]):
        if i == 0:
            column_num += I_A_Z_char_dict[v]
        else:
            column_num += I_A_Z_char_dict[v] * (26 ** i)
    row = regex2.findall(value)[0]
    return int(row), column_num


def get_list_row(max_row):
    """

    :param max_row:
    :return:
    """
    NC_list = []
    i = 0
    j = -1
    while 1:
        if i > 25:
            i = 0
            j += 1
        if j >= 0:
            NC_list.append(A_Z_char[j] + A_Z_char[i])
            if A_Z_char[j] + A_Z_char[i] == max_row:
                break
        else:
            NC_list.append(A_Z_char[i])
            if A_Z_char[i] == max_row:
                break
        i += 1
    return NC_list


def get_row_column(value, start_end="START"):
    if start_end == "START":
        row_value = 1
        column_default = "H"
    else:
        row_value = 691
        column_default = "JW"
    regex = re.compile("[A-Z]+")
    regex2 = re.compile("[0-9]+")
    column = regex.findall(value)[0] if regex.findall(value) else column_default
    row = regex2.findall(value)[0] if regex2.findall(value) else row_value
    return column, int(row)


